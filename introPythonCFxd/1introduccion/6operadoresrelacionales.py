numero_uno = 10
numero_dos = 20

print(numero_uno, ">", numero_dos, "=", numero_uno > numero_dos)
print(numero_uno, "<", numero_dos, "=", numero_uno < numero_dos)
print(numero_uno, ">=", numero_dos, "=", numero_uno >= numero_dos)
print(numero_uno, "<=", numero_dos, "=", numero_uno <= numero_dos)
print(numero_uno, "==", numero_dos, "=", numero_uno == numero_dos)
print(numero_uno, "!=", numero_dos, "=", numero_uno != numero_dos)