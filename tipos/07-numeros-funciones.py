import math

print(round(1.3))
# Redondea el valor al número entero más cercano. Si es .5, se redondea hacia arriba
print(abs(-45))
print(math.ceil(1.3))
# Redondea el valor hacia el número entero más cercano hacia arriba, no importa el valor del decimal
print(math.floor(2.9))
# Redondea el valor hacia el número entero más cercano hacia abajo, no importa el valor del decimal
print(math.isnan(23))
# Arroja False si le pasamos un número
print(math.pow(10, 3))
# Para elevar un número a una cierta potencia
print(math.sqrt(9))
# Para sacar la raíz cuadrada de un número

